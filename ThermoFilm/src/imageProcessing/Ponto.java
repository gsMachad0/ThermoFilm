package imageProcessing;

/**
 *
 * @author Guilherme
 */
public class Ponto {
    int x, y;
    public Ponto (int i, int j){
        x = i;
        y= j;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
